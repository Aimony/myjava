public class NMrint {
    public static void main(String[] args) {
        double d = 100.675;
        double e = 100.500;
        double f = 100.200;
 
        System.out.println(Math.rint(d));
        System.out.println(Math.rint(e)); 
        System.out.println(Math.rint(f)); 
    }
}
/**
rint() 方法返回最接近参数的整数值。
返回 double 类型数组，是最接近参数的整数值。
 */