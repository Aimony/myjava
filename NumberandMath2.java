public class NumberandMath2 {
    public static void main(String[] args) {
        System.out.println("sin(π/2) = " + Math.sin(Math.PI/2));
        System.out.println("cos(0) = " + Math.cos(0));
        System.out.println("tan(60) = " + Math.tan(60));
        System.out.println("arctan(1) = " + Math.atan(1));
        System.out.print("π/2的角度 : " + Math.toDegrees(Math.PI/2));
        System.out.println(Math.PI);
    }
}
